# 4.	Given numbers = [10, 20, 30, 40, 50, 60, 70], print:
# 	First 3 elements
# 	Last 2 elements
# 	Elements from index 2 to 5 (excluding 5)
# 	Reverse the list using slicing

numbers = [10, 20, 30, 40, 50, 60, 70]
print("First 3 elements of list: ",numbers[0:3])
print("Last 2 elements of list: ",numbers[4:6])
print("Elements from index 2 to 5: ",numbers[2:5])
print("Reverse the list: ",numbers[::-1])